// Generate the real-geography world map used by the admin live-visitor map.
//
// Source: PUBLIC-DOMAIN Natural Earth via the `world-atlas` package (TopoJSON).
// We project the country polygons with a plain equirectangular (Plate Carrée)
// projection into the SAME 1000x500 viewBox the live map uses, so the generated
// land lines up EXACTLY with the visitor markers (which are placed with
// world-geo.ts `projectEquirect`). Output is a committed, dependency-free static
// module (world-vector.ts) — the CMS admin never fetches map geometry at runtime
// (CSP-safe) and the geometry ships once inside a cached client chunk.
//
//   node scripts/gen-world-vector.mjs [110|50]   (default 110 — the shipped resolution)
//
// d3-geo / topojson-client / world-atlas are devDependencies used ONLY here; they
// are installed ad-hoc for regeneration (not kept in the repo dep tree). Install:
//   pnpm add -Dw d3-geo topojson-client world-atlas   (then remove after)

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import { geoEquirectangular, geoPath } from "d3-geo";
import { merge, mesh } from "topojson-client";

const __dirname = dirname(fileURLToPath(import.meta.url));
const res = process.argv[2] === "50" ? "50" : "110";
const W = 1000;
const H = 500;

const topoPath = resolve(__dirname, `../node_modules/world-atlas/countries-${res}m.json`);
const topo = JSON.parse(readFileSync(topoPath, "utf8"));
const countries = topo.objects.countries;

// Plate Carrée fitted to 1000x500. This is IDENTICAL to world-geo.ts
// projectEquirect: x = (lng+180)/360*W, y = (90-lat)/180*H.
const projection = geoEquirectangular()
  .scale(W / (2 * Math.PI))
  .translate([W / 2, H / 2]);

// Assert the projection matches projectEquirect at reference points, so markers
// and land can never silently drift apart.
function projectEquirect(lat, lng) {
  return [((lng + 180) / 360) * W, ((90 - lat) / 180) * H];
}
for (const [lat, lng] of [
  [0, 0], [22, 79], [51.1, 10.4], [39.5, -98.35], [-25.3, 133.8], [64.9, -18.6],
]) {
  const a = projection([lng, lat]);
  const b = projectEquirect(lat, lng);
  if (Math.abs(a[0] - b[0]) > 0.01 || Math.abs(a[1] - b[1]) > 0.01) {
    throw new Error(`Projection mismatch at ${lat},${lng}: d3=${a} vs equirect=${b}`);
  }
}

// A geoPath context that rounds to 1 decimal (≈0.0007° at this scale — sub-pixel
// when the widget renders ~1400px wide) to keep the committed string compact.
function roundingContext() {
  let s = "";
  const r = (n) => {
    const v = Math.round(n * 10) / 10;
    return Object.is(v, -0) ? "0" : String(v);
  };
  return {
    _: () => s,
    beginPath() { s = ""; },
    moveTo(x, y) { s += `M${r(x)} ${r(y)}`; },
    lineTo(x, y) { s += `L${r(x)} ${r(y)}`; },
    arc() { /* only used for point geometries; unused here */ },
    closePath() { s += "Z"; },
  };
}

function toPath(geojson) {
  const ctx = roundingContext();
  const path = geoPath(projection, ctx);
  path(geojson);
  return ctx._();
}

// Union of all countries → clean land fill with no internal seams. Stroking this
// same path yields the coastline, so we don't emit a separate coast path.
const land = merge(topo, countries.geometries);
// Interior borders only (a !== b) — drawn as a fainter stroke over the fill.
const borders = mesh(topo, countries, (a, b) => a !== b);

const LAND = toPath(land);
const BORDERS = toPath(borders);

const out = `/**
 * Real-geography world map — GENERATED by scripts/gen-world-vector.mjs, do not hand-edit.
 * Source: PUBLIC-DOMAIN Natural Earth (world-atlas countries-${res}m), projected
 * equirectangular (Plate Carrée) into the 1000×500 viewBox — pixel-aligned with
 * world-geo.ts \`projectEquirect\` so visitor markers sit on the right countries.
 * Regenerate: \`node scripts/gen-world-vector.mjs ${res}\`.
 */
export const WORLD_VIEWBOX = { w: ${W}, h: ${H} } as const;
export const WORLD_RESOLUTION = "${res}m" as const;

/** Union of all landmasses — one filled shape; stroke it for the coastline. */
export const WORLD_LAND_PATH =
  "${LAND}";

/** Interior country borders (fainter stroke over the fill). */
export const WORLD_BORDERS_PATH =
  "${BORDERS}";
`;

const outPath = resolve(__dirname, "../apps/dashboard/src/admin/analytics/world-vector.ts");
writeFileSync(outPath, out);

const kb = (str) => (Buffer.byteLength(str, "utf8") / 1024).toFixed(1);
console.log(`Wrote world-vector.ts (${res}m)`);
console.log(`  land    ${kb(LAND)} KB`);
console.log(`  borders ${kb(BORDERS)} KB`);
console.log(`  file    ${kb(out)} KB total`);
